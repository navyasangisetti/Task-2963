package javaTask1;

public class if_elseifprogram {
	public static void main(String args[]) {
		int token = 10;
		if (token == 5) {
			System.out.println("Blue bottle");
		}
		else if (token == 10) {
			System.out.println("Red bottle");
		}
		else {
			System.out.println("Pink bottle");
		}
	}
     
}
